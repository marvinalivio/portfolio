let menu = document.getElementById("menu");
let closeNav = document.getElementById("closeNav");
function openNav(){
    menu.style.top = "0";
    closeNav.style.display = "inline-block"
}

function closeNow(){
    menu.style.top = "-100vh"
    closeNav.style.display = "none"
}

function closeNo(){
    menu.style.top = "-100vh"
    closeNav.style.display = "none"
}